$stmtArticle = $pdo->prepare('
    INSERT INTO articles (title, description, content, image, slug)
    VALUES (:title, :description, :content, :image, :slug)
');

$stmtCategory = $pdo->prepare('
    SELECT id FROM categories WHERE slug = :slug
');

$stmtPivot = $pdo->prepare('
    INSERT INTO article_category (article_id, category_id)
    VALUES (:article_id, :category_id)
');

foreach ($articles as $article) {
    $categorySlugs = $article['categories'];
    unset($article['categories']);

    $stmtArticle->execute($article);
    $articleId = (int)$pdo->lastInsertId();

    foreach ($categorySlugs as $catSlug) {
        $stmtCategory->execute(['slug' => $catSlug]);
        $categoryId = (int)$stmtCategory->fetchColumn();

        if ($categoryId) {
            $stmtPivot->execute([
                'article_id'  => $articleId,
                'category_id' => $categoryId,
            ]);
        }
    }
}

